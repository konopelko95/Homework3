package qa.guru;

public class Interior {
    int crust;
    int rigidMantle;
    int stifferMantle;
    int outerCore;
    int innerCore;

    public int getCrust() {
        return crust;
    }

    public void setCrust(int crust) {
        this.crust = crust;
    }

    public int getRigidMantle() {
        return rigidMantle;
    }

    public void setRigidMantle(int rigidMantle) {
        this.rigidMantle = rigidMantle;
    }

    public int getStifferMantle() {
        return stifferMantle;
    }

    public void setStifferMantle(int stifferMantle) {
        this.stifferMantle = stifferMantle;
    }

    public int getOuterCore() {
        return outerCore;
    }

    public void setOuterCore(int outerCore) {
        this.outerCore = outerCore;
    }

    public int getInnerCore() {
        return innerCore;
    }

    public void setInnerCore(int innerCore) {
        this.innerCore = innerCore;
    }
}

package qa.guru;

public class Shell {
        int exosphere;
        int thermosphere;
        int mesosphere;
        int stratosphere;
        int troposhere;

        public int getExosphere() {
            return exosphere;
        }

        public void setExosphere(int exosphere) {
            this.exosphere = exosphere;
        }

        public int getThermosphere() {
            return thermosphere;
        }

        public void setThermosphere(int thermosphere) {
            this.thermosphere = thermosphere;
        }

        public int getMesosphere() {
            return mesosphere;
        }

        public void setMesosphere(int mesosphere) {
            this.mesosphere = mesosphere;
        }

        public int getStratosphere() {
            return stratosphere;
        }

        public void setStratosphere(int stratosphere) {
            this.stratosphere = stratosphere;
        }

        public int getTroposhere() {
            return troposhere;
        }

        public void setTroposhere(int troposhere) {
            this.troposhere = troposhere;
        }
}
